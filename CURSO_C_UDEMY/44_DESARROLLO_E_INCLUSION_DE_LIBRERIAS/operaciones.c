#include "operaciones.h"

int suma(int num1, int num2){
    return num1 + num2;
}

int resta(int num1, int num2){
    return num1 - num2;
}

int multiplicacion(int num1, int num2){
    return num1 * num2;
}

float division(int num1, int num2){
    //if(num2 == 0) return 0;
    //else return (num1 / num2);
    return num1 / num2;
}